namespace SampSharp.Entities.SAMP;

/// <summary>
/// Contains all available body parts.
/// </summary>
/// <remarks>
/// See <see href="https://www.open.mp/docs/scripting/resources/bodyparts">https://www.open.mp/docs/scripting/resources/bodyparts</see>.
/// </remarks>
public enum BodyPart
{
    /// <summary>
    /// The chest.
    /// </summary>
    Chest = 3,

    /// <summary>
    /// The crotch.
    /// </summary>
    Crotch = 4,

    /// <summary>
    /// The left arm.
    /// </summary>
    LeftArm = 5,

    /// <summary>
    /// The right arm.
    /// </summary>
    RightArm = 6,

    /// <summary>
    /// The left leg.
    /// </summary>
    LeftLeg = 7,

    /// <summary>
    /// The right leg.
    /// </summary>
    RightLeg = 8,

    /// <summary>
    /// The head.
    /// </summary>
    Head = 9
}