    /// </remarks>
    public enum Bone
    {
        /// <summary>
        ///     The spine.
        /// </summary>
        Spine = 1,

        /// <summary>
        ///     The head.
        /// </summary>
        Head = 2,

        /// <summary>
        ///     The left upper arm.
        /// </summary>
        LeftUpperArm = 3,

        /// <summary>
        ///     The right upper arm.
        /// </summary>
        RightUpperArm = 4,

        /// <summary>
        ///     The left hand.
        /// </summary>
        LeftHand = 5,

        /// <summary>
        ///     The right hand.
        /// </summary>
        RightHand = 6,

        /// <summary>
        ///     The left thigh.
        /// </summary>
        LeftThigh = 7,

        /// <summary>
        ///     The right thigh.
        /// </summary>
        RightThigh = 8,

        /// <summary>
        ///     The left foot.
        /// </summary>
        LeftFoot = 9,

        /// <summary>
        ///     The right foot.
        /// </summary>
        RightFoot = 10,

        /// <summary>
        ///     The right calf.
        /// </summary>
        RightCalf = 11,

        /// <summary>
        ///     The left calf.
        /// </summary>
        LeftCalf = 12,

        /// <summary>
        ///     The left forearm.
        /// </summary>
        LeftForearm = 13,

        /// <summary>
        ///     The right forearm.
        /// </summary>
        RightForearm = 14,

        /// <summary>
        ///     The left clavicle.
        /// </summary>
        LeftClavicle = 15,

        /// <summary>
        ///     The right clavicle.
        /// </summary>
        RightClavicle = 16,

        /// <summary>
        ///     The neck.
        /// </summary>
        Neck = 17,

        /// <summary>
        ///     The jaw.
        /// </summary>
        Jaw = 18
    }
}