namespace GameMode.Definitions
{
    public enum MapIconType
    {
        /// <summary>
        /// Displays in the player's local are.
        /// </summary>
        Local=0,
        /// <summary>
        /// Displays always.
        /// </summary>
        Global=1,
        /// <summary>
        /// Displays in the player's local area and has a checkpoint marker.
        /// </summary>
        LocalCheckPoint=2,
        /// <summary>
        /// Displays always and has a checkpoint marker.
        /// </summary>
        GlobalCheckPoint=3
    }
}
