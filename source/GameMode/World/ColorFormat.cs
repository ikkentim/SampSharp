namespace GameMode.World
{
    /// <summary>
    /// Contains different formats of String representations of Color instances.
    /// </summary>
    public enum ColorFormat
    {
        /// <summary>
        /// {RRGGBBAA}
        /// </summary>
        RGBA,
        /// <summary>
        /// {AARRGGBB}
        /// </summary>
        ARGB,
        /// <summary>
        /// {RRGGBB}
        /// </summary>
        RGB
    }
}
